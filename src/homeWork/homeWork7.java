package homeWork;

public class homeWork7 {
    public static void main(String[] args) {
//  Необходимо вывести на экран таблицу умножения на 3 с помощью цикла for:
//  3*1=3
//  3*2=6
//  3*3=9
//  3*4=12

        for (int i = 1; i < 10; i += 1) {
            System.out.println("3*" + i + "=" + (i * 3));
        }
    }
}
